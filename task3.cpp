#include "pch.h"
#include <iostream>
#include <vector>

using namespace std;

int main()
{
	int n;
	int d;
	int m = 0;
	int day = 0;
	int count = 0;


	cin >> n >> d;
	vector <int> a(n);
	vector <int> answer;
	for (int i = 0; i < n; i++) {
		cin >> a[i];
	}

	m = a[0];
	for (int i = 0; i < n; i++) {
		day = m + d;
		if (a[i] > day) {
			answer.push_back(day);
			count++;
			m = a[i];
		}
	}
	cout << count + 1 << endl;
	for (int i = 0; i < count; i++) {
		cout << answer[i] << " ";
	}
	cout << day;
}

